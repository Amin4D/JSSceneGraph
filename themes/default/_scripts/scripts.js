

// ------------ Function to show the grid view ------------ //
function oToggleGridView(){
	document.getElementById('projects-grid').style.display = 'block';
	document.getElementById('projects-list').style.display = 'none';	
}

// ------------ Function to show the list view ------------ //
function oToggleListView(){
	document.getElementById('projects-grid').style.display = 'none';
	document.getElementById('projects-list').style.display = 'block';	
}



